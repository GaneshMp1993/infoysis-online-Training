package com.project.operators;

public class LogicalOperatorDemo {
	public static void main(String[] args) {
		boolean a=false;
		boolean b=false;
		System.out.println("Result of (A&&B)="+(a&&b));//and(&) operator
		System.out.println("Result of (A||B)="+(a||b));//OR(||) operator
		System.out.println("Result of (!(a||b))"+!(a||b));//!->Natation operator
		
	}

}
